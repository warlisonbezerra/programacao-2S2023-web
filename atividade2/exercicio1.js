let num1 = 10;
let num2 = 5;
let resultado;

resultado = num1 + num2;
console.log(`Resultado da adição entre ${num1} e ${num2} = ${resultado}`);

resultado = num1 - num2;
console.log(`Resultado da subtração entre ${num1} e ${num2} = ${resultado}`);

resultado = num1 * num2;
console.log(`Resultado da multiplicação entre ${num1} e ${num2} = ${resultado}`);

resultado = num1 / num2;
console.log(`Resultado da divisão entre ${num1} e ${num2} = ${resultado}`);

resultado = num1 % num2;
console.log(`Resultado do módulo entre ${num1} e ${num2} = ${resultado}`);

num1++;
resultado = num1;
console.log(`Resultado do incremento de ${num1 - 1} é ${resultado}`);

num2--;
resultado = num2;
console.log(`Resultado do decremento de ${num2 + 1} é ${resultado}`);
